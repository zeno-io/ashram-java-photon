  /**
   * Definition for singly-linked list. public class ListNode { int val; ListNode next; ListNode(int
   * x) { val = x; } }
   */
  class Solution {

    public ListNode getKthFromEnd(ListNode head, int k) {
      ListNode f = head;
      int i = 0;
      while (f != null && i < k) {
        f = f.next;
        i++;
      }
      if (i < k) {
        return head;
      }
      ListNode s = head;
      while (f != null) {
        s = s.next;
        f = f.next;
      }
      return s;
    }

  }
